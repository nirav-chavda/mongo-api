var mongoose = require('mongoose');

// Define a schema
// todos : Name of the collection
var Todo = mongoose.model('todos',{
  text: {
    type: String,
    required: true,
    minlength: 1,
    trim: true
  },
  completed: {
    type: Boolean,
    default: false
  },
  completedAt: {
    type: Number,
    default: null
  }
});

module.exports = {Todo};
